#include <stdio.h>

int main() {
    unsigned char bytes[] = {0xB8, 0x0C, 0x00, 0x00, 0x00, 0xBF, 0x01, 0x00, 0x00, 0x00, 0x48, 0x8D, 0x3D, 0x00, 0x00, 0x00, 0x00, 0xBa, 0x0D, 0x00, 0x00, 0x00, 0xCD, 0x80, 0xC3};

    // Example: Writing bytes to a file (optional, if needed)
    FILE *file = fopen("output.bin", "wb");
    if (file != NULL) {
        fwrite(bytes, sizeof(bytes), 1, file);
        fclose(file);
    }

    return 0;
}
