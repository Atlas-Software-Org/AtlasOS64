__attribute__((interrupt)) void KeyboardInt_Hndlr(struct InterruptFrame* Frame) {
    uint8_t scancode = inb(0x60);
    uint8_t CharIdx = scancode;
    int state;
    switch (scancode) {
        case 0x2A: // LShift
            state = 0b0001;
            break;
        case 0xAA: // LShift - Release
            state = 0b0000;
            break;
        case 0x3A: // CapsLock
            state = 0b1110;
    }

    PIC_sendEOI(1);
}