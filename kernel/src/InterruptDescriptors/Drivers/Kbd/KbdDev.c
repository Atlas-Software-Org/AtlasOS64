#include "KbdDev.h"

bool isReading;
int KbdIdx = 0;
char buffer[65535];

char getch() {
    isReading = true;
    int LastKbdIdx = KbdIdx;
    while (LastKbdIdx == KbdIdx);  // Wait for a new character
    isReading = false;
    return buffer[KbdIdx];
}

char* getstrn(size_t count, uint64_t x_box, uint64_t y_box, uint32_t box_back_color) {
    bool finished = false;
    size_t counter = 0;
    memset(buffer, 0, sizeof(buffer));
    KbdIdx = 0;
    
    int64_t xoff = x_box, yoff = y_box;  // Initialize cursor positions

    while (!finished && counter < count - 1) {
        char chr = getch();

        if (chr == '\n') {
            break;
        } else if (chr == '\b') {
            uint32_t temp_clr = ClearColor;
            ClearColor = box_back_color;
            FontPutChar('\b', xoff, yoff, box_back_color);
            ClearColor = temp_clr;
        } else {
            buffer[counter++] = chr;
            xoff += 8;  // Move cursor forward
        }
    }

    buffer[counter] = '\0';  // Null-terminate the string
    return buffer;
}

